// Create an array
let fruits = ["apple", "banana", "mango", "grapes"];

// Access elements
console.log(fruits[0]); // apple
console.log(fruits[2]); // mango

// Add new item
fruits.push("orange");

// Remove last item
fruits.pop();

// Loop through array
for (let i = 0; i < fruits.length; i++) 
    {
    console.log(fruits[i]);
}

// Length of array
console.log("Total fruits:", fruits.length);
