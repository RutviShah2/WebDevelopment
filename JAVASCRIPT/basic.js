// Basic JavaScript Example

// 1. Print a message
console.log("Hello, Rutvii! Welcome to JavaScript!");

// 2. Variables
let name = "Rutvii";
let age = 18;

// 3. Arithmetic operations
let num1 = 10;
let num2 = 5;

let sum = num1 + num2;
let diff = num1 - num2;
let product = num1 * num2;
let division = num1 / num2;

// 4. Display results
console.log("Name:", name);
console.log("Age:", age);
console.log("Sum:", sum);
console.log("Difference:", diff);
console.log("Product:", product);
console.log("Division:", division);

// 5. Conditional statement
if (age >= 18) 
    {
    console.log("You’re an adult now! ");
} 
else
     {
    console.log("You’re still growing — enjoy it! ");
}

// 6. Function
function greet(person) 
{
    return "Hey " + person + ", have a beautiful day ahead! ";
}

console.log(greet(name));
